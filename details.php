<b><u>National Identity Card issuance.</u/></b>
<br><br>

<b>Normal Service</b>	
	<ul>
		<li>National Identity Cards will be sent by post once applications, submitted through the Principal, are forwarded to the department or relevant provincial offices.</li>
        <li>For applications submitted through the Grama Niladhari, the National Identity Card will be sent by post after the application and related documents are forwarded to the Department's system by the NIC Card Division of the respective District Secretariats</li>
	</ul>
<br>
<b>Oneday Service</b>
<ul>
	<li>Applicants seeking one-day service for obtaining National Identity Cards can do so by visiting the Head Office</li>
</ul>
