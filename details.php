<b><u>Amending (Updating) National Identity Card Information</u></b>
<br><br>
&nbsp;&nbsp;&nbsp; If a National Identity Card is damaged, contains incorrect or illegible information, or if there are changes to the information on the ID card, individuals should apply for an amendment immediately.
<br><br>
<b><mark class="new5">&nbsp;&nbsp; Fee  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</mark></b>
